def triangle_area(p1, p2, p3):
    """Calculate the area of a triangle given its three vertices."""
    v1 = p2 - p1
    v2 = p3 - p1
    cross = np.cross(v1, v2)
    return 0.5 * np.linalg.norm(cross)

def mesh_surface_area(grid_data, mesh_elements):
    """Calculate the total surface area of the mesh."""
    total_area = 0
    for element_id, nodes in mesh_elements.items():
        p1 = np.array(grid_data[nodes[0]])
        p2 = np.array(grid_data[nodes[1]])
        p3 = np.array(grid_data[nodes[2]])
        total_area += triangle_area(p1, p2, p3)
    return total_area

def parse_coordinate(coord_str):
    coord_str = coord_str.strip()
    if coord_str == '0.0':
        return 0.0
    elif '-' in coord_str[1:]:  # Check for scientific notation like '-6.873-2'
        base, exp = coord_str.rsplit('-', 1)
        return float(base) * (10 ** -int(exp))
    else:
        return float(coord_str)

def read_grid_data_from_string(data_str):
    grid_data = {}
    for line in data_str.split('\n'):
        if line.startswith('GRID'):
            parts = line.split(None, 2)
            if len(parts) != 3:
                continue
            node_id = int(parts[1])
            coords_str = parts[2]
            x = parse_coordinate(coords_str[:8])
            y = parse_coordinate(coords_str[8:16])
            z = parse_coordinate(coords_str[16:])
            grid_data[node_id] = [x, y, z]
    return grid_data

def parse_mesh_data(mesh_data_str):
    mesh_elements = {}
    for line in mesh_data_str.split('\n'):
        if line.startswith('CTRIA3'):
            parts = line.split()
            if len(parts) == 6:
                element_id = int(parts[1])
                node1, node2, node3 = map(int, parts[3:])
                mesh_elements[element_id] = [node1, node2, node3]
    return mesh_elements

def plot_mesh_3d(grid_data, mesh_elements):
    fig = plt.figure(figsize=(12, 10))
    ax = fig.add_subplot(111, projection='3d')

    # Plot triangular elements
    for element_id, nodes in mesh_elements.items():
        for i in range(3):
            start = grid_data[nodes[i]]
            end = grid_data[nodes[(i+1)%3]]
            ax.plot([start[0], end[0]], [start[1], end[1]], [start[2], end[2]], 'b-', linewidth=0.5)

    # Plot nodes
    coordinates = np.array(list(grid_data.values()))
    ax.scatter(coordinates[:, 0], coordinates[:, 1], coordinates[:, 2], c='r', marker='o', s=20)

    ax.set_xlabel('X')
    ax.set_ylabel('Y')
    ax.set_zlabel('Z')
    plt.title('3D Mesh Visualization')
    plt.tight_layout()
    plt.show()

